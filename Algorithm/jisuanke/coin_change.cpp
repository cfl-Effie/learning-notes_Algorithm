/*
在现实生活中,我们经常遇到硬币找零的问题,例如,在发工资时,财务人员就需要计算最少的找零硬币数,以便他们能从银行拿回最少的硬币数,并保证能用这些硬币发工
资。我们应该注意到,人民币的硬币系统是100,50,20,10,5,2,1,0.5,0.2,0.1,0.05,0.02,0.01元,采用这些硬币,我们可以对任何一个工资数用贪心算法
求出其最少硬币数。但不幸的是:我们可能没有这样一种好的硬币系统,因此用贪心算法不能求出最少的硬币数,甚至有些金钱总数还不能用这些硬币找零。例如,如果硬币
系统是40,30,25元,那么37元就不能用这些硬币找零;95元的最少找零硬币数是3。又如,硬币系统是10,7,5,1元,那么12元用贪心法得到的硬币数为3,而
最少硬币数是2。
你的任务就是:
  对于任意的硬币系统和一个金钱数,请你编程求出最少的找零硬币数。
输入格式
  第一行,两个整数N和M,其中1≤N≤50为硬币系统中不同硬币数,1≤M≤100000为需要用硬币找零的总数。
  第二行为N个整数ai(1≤ai≤1000),它们是硬币系统中各硬币的面值。
输出格式
  请输出最少的找零硬币数,输入不存在输出-1。
eg:
  in:
    4 12
    10 7 5 1
  out:2
*/
#include <iostream>
#include <vector>
#include <algorithm>
#include <climits>
using namespace std;

int main() {
    int N, M;
    cin >> N >> M;
    
    vector<int> coins(N);
    for (int i = 0; i < N; i++) {
        cin >> coins[i];
    }
    
    // dp[i] 表示凑出金额i所需的最少硬币数
    vector<int> dp(M + 1, INT_MAX);
    dp[0] = 0;  // 金额0需要0个硬币
    
    // 动态规划
    for (int i = 1; i <= M; i++) {
        for (int j = 0; j < N; j++) {
            if (coins[j] <= i && dp[i - coins[j]] != INT_MAX) {
                dp[i] = min(dp[i], dp[i - coins[j]] + 1);
            }
        }
    }
    
    // 输出结果
    if (dp[M] == INT_MAX) {
        cout << -1 << endl;
    } else {
        cout << dp[M] << endl;
    }
    
    return 0;
}
